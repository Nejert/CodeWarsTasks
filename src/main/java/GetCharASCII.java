/**
 * <p>Write a function {@code get_char()} / {@code getChar()} which takes a number and returns the corresponding ASCII char for that value.</p>
 * <h4>Example:</h4>
 * <pre>{@code get_char(65)}</pre>
 * <p>should return:</p>
 * <pre>{@code 'A'}</pre>
 * <p>For ASCII table, you can refer to <a href="http://www.asciitable.com/">http://www.asciitable.com/</a></p>
 */
public class GetCharASCII {
    public static char getChar(int c) {
        return (char) c;
    }
}
